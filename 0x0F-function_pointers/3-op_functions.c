#include "3-calc.h"
/**
 * op_add - a function that returns the sum
 * @a: first input number
 * @b: second input number
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_mul - a function that returns the multiplication
 * @a: first input number
 * @b: second input number
 * Return: the multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_sub - a function that returns the sub
 * @a: first input number
 * @b: second input number
 * Return: the sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_div - a function that returns the div
 * @a: first input number
 * @b: second input number
 * Return: the div
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - a function that returns the modular
 * @a: first input number
 * @b: second input number
 * Return: the modulo
 */
int op_mod(int a, int b)
{
	return (a % b);
}
